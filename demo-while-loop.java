package com.mycompany.strukturkontrolpemilihan;

/**
 * Demonstrasi While Loop
 * @author hp
 * @contributor luqmanaru
 */
public class PerulanganWhile {
    public static void main(String[] args) {
        int x = 0; // Inisialisasi counter
        
        // While loop dengan kondisi
        while (x < 10) {
            System.out.println(x);
            x++; // Increment counter
        }
    }
}
